package modelo;

/**
 *
 * @author Sarai
 */
public class Rol {
    private String nombre;

    public Rol(String nombre) {
        this.nombre = nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    @Override
    public String toString() {
        return "Rol{" + "nombre=" + nombre + '}';
    }
    
    
}
