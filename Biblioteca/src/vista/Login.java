    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String correo = txtCorreo.getText();
    String contrasena = new String(txtPassword.getPassword());

    try {
        UsuarioControlador controlador = new UsuarioControlador(conn);
        Usuario usuario = controlador.buscarPorCorreoYContrasena(correo, contrasena);

        if (usuario != null) {
            
            JOptionPane.showMessageDialog(this, "Bienvenid@ " + usuario.getNombre());
           new IMenu(correo).setVisible(true);
                
            dispose();
        } else {
            // Mostrar mensaje de error
                lblError.setText("Correo o contraseña incorrectos.");
                lblError.setForeground(Color.RED);
                lblError.setVisible(true);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error de conexión: " + e.getMessage());
    }
    }//GEN-LAST:event_btnLoginActionPerformed

