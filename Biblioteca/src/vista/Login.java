    private void lblOjoContraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblOjoContraMouseClicked
        // TODO add your handling code here:
        passwordVisible = !passwordVisible;
        
        if (passwordVisible) {
            lblOjoContra.setIcon(iconoOjoAbierto);
            txtPassword.setEchoChar((char) 0);
        } else {
            lblOjoContra.setIcon(iconoOjoCerrado);
             txtPassword.setEchoChar('*');
        }
    }//GEN-LAST:event_lblOjoContraMouseClicked

