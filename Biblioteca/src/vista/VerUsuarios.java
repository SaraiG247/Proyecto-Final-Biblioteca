    private void configurarBarraBusqueda() {
        // Configurar el listener para la barra de búsqueda
        barraBusqueda1.getCampoBusqueda().getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                filtrarTabla();
            }
            @Override
            public void removeUpdate(DocumentEvent e) {
                filtrarTabla();
            }
            @Override
            public void changedUpdate(DocumentEvent e) {
                filtrarTabla();
            }
        });
    }
    
    private void filtrarTabla() {
    String textoBusqueda = barraBusqueda1.getCampoBusqueda().getText().toLowerCase();
    DefaultTableModel modelo = (DefaultTableModel) tblUser.getModel();
    modelo.setRowCount(0); // Limpiar tabla

    try {
        Connection conexion = ConexionDB.getConexion();
        UsuarioControlador controlador = new UsuarioControlador(conexion);

        for (Usuario usuario : controlador.buscarTodos()) {
            if (textoBusqueda.isEmpty()
                || usuario.getNombre().toLowerCase().contains(textoBusqueda)
                || usuario.getApellidos().toLowerCase().contains(textoBusqueda)
                || usuario.getTelefono().toLowerCase().contains(textoBusqueda)
                || usuario.getCorreo().toLowerCase().contains(textoBusqueda)
                || usuario.getRol().toLowerCase().contains(textoBusqueda)) {

                modelo.addRow(new Object[] {
                    usuario.getNombre(),
                    usuario.getApellidos(),
                    usuario.getTelefono(),
                    usuario.getCorreo(),
                    usuario.getRol()
                });
            }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al filtrar usuarios: " + e.getMessage());
        e.printStackTrace();
    }
}

   
    public void cargarUsuariosEnTabla() {
    DefaultTableModel modelo = new DefaultTableModel(
        new Object[][] {},
        new String[] { "Nombres", "Apellidos", "Teléfono", "Correo", "Rol" }
    );
    tblUser.setModel(modelo);
    modelo.setRowCount(0); // Limpiar tabla

    try {
        Connection conexion = ConexionDB.getConexion();
        UsuarioControlador controlador = new UsuarioControlador(conexion);

        for (Usuario u : controlador.buscarTodos()) {
            modelo.addRow(new Object[] {
                u.getNombre(),
                u.getApellidos(),
                u.getTelefono(),
                u.getCorreo(),
                u.getRol()
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar usuarios: " + e.getMessage());
        e.printStackTrace();
    }
}

    


