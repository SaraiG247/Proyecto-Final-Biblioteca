    private void btnEditarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarUActionPerformed
       int filaSeleccionada = tblUser.getSelectedRow();

    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Por favor selecciona un libro para editar.");
        return;
    }

    String nombres = tblUser.getValueAt(filaSeleccionada, 0).toString();
    String apellidos = tblUser.getValueAt(filaSeleccionada, 1).toString();
    String telefono = tblUser.getValueAt(filaSeleccionada, 2).toString();
     String correo = tblUser.getValueAt(filaSeleccionada, 3).toString();
    String tipoUsuario = tblUser.getValueAt(filaSeleccionada, 4).toString();

    // Crear y mostrar FrmLibros con los datos cargados
    FrmUsuario formulario= new FrmUsuario();
    formulario.setModoEdicion(true); // opcional, si quieres un flag para saber que es edición
    formulario.setDatosUsuario(nombres, apellidos, telefono, correo, tipoUsuario);
    
        formulario.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        formulario.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                // recarga combos después
                cargarUsuariosEnTabla();    
            }
        });
    
    formulario.setVisible(true);
    }//GEN-LAST:event_btnEditarUActionPerformed

    private void btnEliminarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarUActionPerformed
        int filaSeleccionada = tblUser.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un usuario en la tabla para eliminar.");
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este usuario?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirmacion != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Connection conexion = ConexionDB.getConexion();
    UsuarioControlador controlador = new UsuarioControlador(conexion);
            // Obtén el correo desde la tabla
            String correo = (String) tblUser.getValueAt(filaSeleccionada, 3); // columna 3 = correo

            if (controlador.eliminarPorCorreo(correo)) {
                DefaultTableModel modelo = (DefaultTableModel) tblUser.getModel();
                modelo.removeRow(filaSeleccionada);
                cargarUsuariosEnTabla();
                JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente.");
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el usuario.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarUActionPerformed

