    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       int filaSeleccionada = tblMultas.getSelectedRow();

    if (filaSeleccionada != -1) {
        int idPrestamo = (int) tblMultas.getValueAt(filaSeleccionada, 0); // Columna idPrestamo

        try {
            Connection conexion = ConexionDB.getConexion(); // tu clase de conexión
        MultaControlador controladorMulta = new MultaControlador(conexion);
            boolean exito = controladorMulta.marcarMultaComoPagadaPorPrestamo(idPrestamo);
            if (exito) {
                JOptionPane.showMessageDialog(this, "Multa marcada como pagada.");
                cargarTablaMultas(); // Recargar tabla
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo marcar la multa.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al actualizar multa.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecciona una multa primero.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

