    private void cargarTablaMultas() {
    DefaultTableModel modelo = (DefaultTableModel) tblMultas.getModel();
    modelo.setRowCount(0); // Limpiar la tabla

    try {
        Connection conexion = ConexionDB.getConexion(); // tu clase de conexión
        MultaControlador controladorMulta = new MultaControlador(conexion);
        List<Multa> lista = controladorMulta.listarMultas(); // Usa tu clase controladora real
        for (Multa m : lista) {
            modelo.addRow(new Object[]{
                m.getIdPrestamo(),
                m.getMonto(),
                m.isPagado() ? "SI" : "NO",
                m.getFechaMulta()
            });
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al cargar multas.");
    }
}

    
