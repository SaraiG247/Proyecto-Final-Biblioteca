    public void cargarTablaPrestamos() {
    DefaultTableModel modelo = new DefaultTableModel(
        new Object[][]{},
        new String[]{"ID","Cantidad total", "Fecha préstamo", "Fecha devolución", "Estado"}
    );
    tblPrestamo.setModel(modelo);

    try {
        Connection conn = ConexionDB.getConexion();
        String sql = "SELECT idPrestamo, cantidadTotal, fechaPrestamo, fechaDevolucion, status FROM prestamo ORDER BY fechaPrestamo DESC";
        try (Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("idPrestamo"),
                    rs.getInt("cantidadTotal"),
                    rs.getDate("fechaPrestamo"),
                    rs.getDate("fechaDevolucion"),
                    rs.getString("status")
                });
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error al cargar tabla de préstamos: " + e.getMessage());
    }
}

    
    
    private void actualizarPrestamosVencidos() {
    try {
        Connection conn = ConexionDB.getConexion();
        PrestamoControlador prestamoControlador = new PrestamoControlador(conn);
        int actualizados = prestamoControlador.actualizarPrestamosAMulta();
        
        if (actualizados > 0) {
            JOptionPane.showMessageDialog(null, 
                actualizados + " préstamos han sido actualizados a 'Multa' por retraso y se generaron los datos de multa",
                "Préstamos actualizados", 
                JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, 
            "Error al actualizar préstamos vencidos: " + ex.getMessage(),
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    }
}
    
    
