    private void btnEntregadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntregadoActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tblPrestamo.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(null, "Por favor seleccione un préstamo");
            return;
        }
        
        try {
            Connection conn = ConexionDB.getConexion();
        PrestamoControlador prestamoControlador = new PrestamoControlador(conn);
            // Obtener el ID del préstamo (asumiendo que tienes una columna oculta con el ID)
            int idPrestamo = (int) tblPrestamo.getValueAt(filaSeleccionada, 0);
            
            if (prestamoControlador.marcarComoEntregado(idPrestamo)) {
                JOptionPane.showMessageDialog(null, "Préstamo marcado como entregado");
                cargarTablaPrestamos(); // Refrescar la tabla
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo actualizar el préstamo");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al actualizar: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnEntregadoActionPerformed

